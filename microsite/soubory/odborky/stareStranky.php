		Podmínkou pro šátkování je získání alespoň 5 stupňů libovolných odborek, tj. např.:
	<ul>
		<li>prvních stupňů různých odborek</li>
		<li>jednoho třetího stupně jedné odborky a jednoho druhého stupně jiné odborky</li>
	</ul>
	</p>

	<p>
		Podmínkou pro pasování na instruktora je splnění všech třetích stupňů a jednoho vybraného čtvrtého stupně.
