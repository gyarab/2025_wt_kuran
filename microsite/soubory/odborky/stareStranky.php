		Každá odborka má čtyři stupně (1. - nejlehčí, 4. - nejtěžší). Jednotlivé stupně lze plnit postupně, nicméně kdo
		chce splnit např. 3. stupeň,
		musí již mít 1. a 2. stupeň téže odborky (případně splnit 1.-3. najednou).
	</p>

	<p>
		Splnění stupně odborky je odměněno získáním odznáčku odpovídajícího stupně a 3 body do bodování.
